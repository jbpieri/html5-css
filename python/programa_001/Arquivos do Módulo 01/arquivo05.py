#   Operadores e operações matemáticas --> Uma boa prática entre os programadores profissionais é o uso de espaços entre as operações.

#   Soma
print(34 + 56)

#   Subtração
print(34 - 56)

#   Multiplicação
print(34 * 56)

#   Divisão
print(20 / 3)

#   Divisão inteira
print(20 // 3)          # Aresenta apenas a parte inteira

#   Resto da divisão
print(20 % 3)

#   Exponenciação
print(2 ** 6)
print(81 ** 0.5)

